func TestNewComposeData(t *testing.T) {
	app := &state.AppConfig{
		Name:         "testapp",
		Repo:         "https://github.com/test/app.git",
		Branch:       "develop",
		Port:         4000,
		Domain:       "test.example.com",
		CurrentImage: "testapp:v2",
		Headers:      map[string]string{"X-Test": "yes"},
	}
	cfg := &state.GlobalConfig{
		Proxy: "traefik",
	}

	data := NewComposeData(app, cfg)

	if data.AppName != "testapp" {
		t.Errorf("AppName = %q, want 'testapp'", data.AppName)
	}
	if data.ProxyType != "traefik" {
		t.Errorf("ProxyType = %q, want 'traefik'", data.ProxyType)
	}
	if data.Port != 4000 {
		t.Errorf("Port = %d, want 4000", data.Port)
	}
}

func TestWriteCompose(t *testing.T) {
	dir := t.TempDir()
	content := "services:\n  test:\n    image: test\n"
	if err := WriteCompose(dir, content); err != nil {
		t.Fatalf("WriteCompose failed: %v", err)
	}

	data, err := os.ReadFile(filepath.Join(dir, "docker-compose.yml"))
	if err != nil {
		t.Fatalf("ReadFile failed: %v", err)
	}
	if string(data) != content {
		t.Errorf("Written content doesn't match")
	}
}

func TestWriteComposeCreatesDir(t *testing.T) {
	dir := filepath.Join(t.TempDir(), "nested", "dir")
	content := "test: true\n"
	if err := WriteCompose(dir, content); err != nil {
		t.Fatalf("WriteCompose failed for nested dir: %v", err)
	}
	data, _ := os.ReadFile(filepath.Join(dir, "docker-compose.yml"))
	if string(data) != content {
		t.Error("Content mismatch")
	}
}

func TestGenerateEmptyDatabases(t *testing.T) {
	data := &ComposeData{
		AppName:     "simple",
		Image:       "simple:v1",
		Port:        80,
		Domain:      "simple.example.com",
		ProxyType:   "traefik",
		Environment: map[string]string{},
		Headers:     map[string]string{},
		Repo:        "https://github.com/test/app.git",
		Branch:      "main",
		GeneratedAt: "2024-01-15T10:30:00Z",
	}

	yaml := Generate(data)
	if strings.Contains(yaml, "volumes:") {
		// The network section has no volumes: prefix
		if strings.Count(yaml, "volumes:") > 0 {
			lines := strings.Split(yaml, "\n")
			for _, line := range lines {
				if strings.HasPrefix(strings.TrimSpace(line), "volumes:") {
					t.Error("Should not have volumes section when no databases")
				}
			}
		}
	}
	if strings.Contains(yaml, "depends_on:") {
		t.Error("Should not have depends_on when no databases")
	}
}

