func TestNewComposeData_SetsFields(t *testing.T) {
	app := &state.AppConfig{
		Name:         "myapp",
		Repo:         "https://github.com/test/myapp.git",
		Branch:       "develop",
		Port:         5000,
		Domain:       "myapp.test.com",
		CurrentImage: "myapp:latest",
		Headers:      map[string]string{"X-App": "test"},
	}
	cfg := &state.GlobalConfig{Proxy: "caddy"}

	data := NewComposeData(app, cfg)

	if data.Image != "myapp:latest" {
		t.Errorf("Image = %q, want 'myapp:latest'", data.Image)
	}
	if data.Domain != "myapp.test.com" {
		t.Errorf("Domain = %q, want 'myapp.test.com'", data.Domain)
	}
	if data.EnvFile == "" {
		t.Error("EnvFile should be set")
	}
	if len(data.Environment) != 0 {
		t.Error("Environment should start empty")
	}
	if len(data.Databases) != 0 {
		t.Error("Databases should start empty")
	}
}
