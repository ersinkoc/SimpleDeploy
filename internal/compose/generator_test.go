func TestGenerateBasic(t *testing.T) {
	data := &ComposeData{
		AppName:     "myapp",
		Image:       "myapp:latest",
		Port:        3000,
		Domain:      "myapp.example.com",
		ProxyType:   "traefik",
		EnvFile:     "/opt/simpledeploy/apps/myapp/.env",
		Environment: map[string]string{"KEY": "value"},
		Databases:   nil,
		Headers:     map[string]string{"X-Custom": "yes"},
		Repo:        "https://github.com/test/myapp.git",
		Branch:      "main",
		GeneratedAt: "2024-01-15T10:30:00Z",
	}

	yaml := Generate(data)

	if !strings.Contains(yaml, "# Auto-generated by SimpleDeploy") {
		t.Error("Should contain auto-generated header")
	}
	if !strings.Contains(yaml, "container_name: qd-myapp") {
		t.Error("Should contain container name")
	}
	if !strings.Contains(yaml, "image: myapp:latest") {
		t.Error("Should contain image")
	}
	if !strings.Contains(yaml, "simpledeploy:") {
		t.Error("Should use simpledeploy network")
	}
	if !strings.Contains(yaml, "traefik.enable=true") {
		t.Error("Should contain traefik labels")
	}
	if !strings.Contains(yaml, "Host(`myapp.example.com`)") {
		t.Error("Should contain domain rule")
	}
	if !strings.Contains(yaml, "KEY=value") {
		t.Error("Should contain environment variable")
	}
	if !strings.Contains(yaml, "X-Custom=yes") {
		t.Error("Should contain custom header")
	}
	if !strings.Contains(yaml, "simpledeploy.managed=true") {
		t.Error("Should contain metadata label")
	}
}

func TestGenerateWithDatabases(t *testing.T) {
	data := &ComposeData{
		AppName:   "dbapp",
		Image:     "dbapp:v1",
		Port:      8080,
		Domain:    "dbapp.example.com",
		ProxyType: "traefik",
		Databases: []DBService{
			{
				Type:       "mysql",
				Image:      "mysql:8",
				Container:  "mysql",
				Volume:     "/var/lib/mysql",
				VolumeName: "qd-dbapp-mysql-data",
				Env:        map[string]string{"MYSQL_ROOT_PASSWORD": "secret", "MYSQL_DATABASE": "dbapp"},
				HealthCheck: &HealthCheckData{
					Test:     []string{"CMD", "mysqladmin", "ping", "-h", "localhost"},
					Interval: "10s",
					Timeout:  "5s",
					Retries:  5,
				},
			},
			{
				Type:       "redis",
				Image:      "redis:7-alpine",
				Container:  "redis",
				Volume:     "/data",
				VolumeName: "qd-dbapp-redis-data",
				Env:        map[string]string{},
			},
		},
		Headers:     map[string]string{},
		Repo:        "https://github.com/test/dbapp.git",
		Branch:      "main",
		GeneratedAt: "2024-01-15T10:30:00Z",
	}

	yaml := Generate(data)

	if !strings.Contains(yaml, "qd-dbapp-mysql") {
		t.Error("Should contain mysql service")
	}
	if !strings.Contains(yaml, "mysql:8") {
		t.Error("Should contain mysql image")
	}
	if !strings.Contains(yaml, "MYSQL_ROOT_PASSWORD: secret") {
		t.Error("Should contain mysql password")
	}
	if !strings.Contains(yaml, "healthcheck:") {
		t.Error("Should contain healthcheck")
	}
	if !strings.Contains(yaml, "mysqladmin") {
		t.Error("Should contain healthcheck test")
	}
	if !strings.Contains(yaml, "qd-dbapp-redis") {
		t.Error("Should contain redis service")
	}
	if !strings.Contains(yaml, "service_healthy") {
		t.Error("Should depend on healthy mysql")
	}
	if !strings.Contains(yaml, "qd-dbapp-mysql-data:") {
		t.Error("Should contain volume declarations")
	}
}

